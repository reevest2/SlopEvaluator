using SlopEvaluator.Mutations.Fix;
using SlopEvaluator.Mutations.Models;
using SlopEvaluator.Mutations.Services;

namespace SlopEvaluator.Mutations.Fix;

public static class TestWriter
{
    public static async Task WriteTestFileAsync(
        List<KillingTest> tests, string outputPath,
        string sourceFileName, string? existingTestFile = null)
    {
        var className = Path.GetFileNameWithoutExtension(sourceFileName) + "MutationTests";
        var ns = DetectNamespace(existingTestFile);

        var sb = new System.Text.StringBuilder();

        sb.AppendLine("// ═══════════════════════════════════════════════════════════════");
        sb.AppendLine("//  AUTO-GENERATED BY SlopEvaluator.Mutations.Fix — Killing tests for surviving mutants");
        sb.AppendLine($"//  Source: {sourceFileName}");
        sb.AppendLine($"//  Generated: {DateTime.UtcNow:yyyy-MM-dd HH:mm} UTC");
        sb.AppendLine("//  Review and complete TODO sections before committing.");
        sb.AppendLine("// ═══════════════════════════════════════════════════════════════");
        sb.AppendLine();

        // Detect usings from existing test file
        if (existingTestFile is not null && File.Exists(existingTestFile))
        {
            var existingUsings = File.ReadAllLines(existingTestFile)
                .Where(l => l.TrimStart().StartsWith("using "))
                .Distinct()
                .ToList();
            foreach (var u in existingUsings)
                sb.AppendLine(u);
        }
        else
        {
            sb.AppendLine("using Xunit;");
        }

        sb.AppendLine();
        if (ns is not null)
        {
            sb.AppendLine($"namespace {ns};");
            sb.AppendLine();
        }

        sb.AppendLine($"/// <summary>");
        sb.AppendLine($"/// Killing tests for surviving mutants in {sourceFileName}.");
        sb.AppendLine($"/// Each test targets a specific mutation that the existing test suite missed.");
        sb.AppendLine($"/// </summary>");
        sb.AppendLine($"public class {className}");
        sb.AppendLine("{");

        for (int i = 0; i < tests.Count; i++)
        {
            if (i > 0) sb.AppendLine();
            sb.AppendLine($"    {tests[i].TestCode}");
        }

        sb.AppendLine("}");

        var dir = Path.GetDirectoryName(outputPath);
        if (dir is not null && !Directory.Exists(dir))
            Directory.CreateDirectory(dir);

        await File.WriteAllTextAsync(outputPath, sb.ToString());
    }

    private static string? DetectNamespace(string? testFile)
    {
        if (testFile is null || !File.Exists(testFile)) return null;

        foreach (var line in File.ReadLines(testFile))
        {
            var trimmed = line.Trim();
            if (trimmed.StartsWith("namespace "))
            {
                return trimmed.Replace("namespace ", "").TrimEnd(';', ' ', '{');
            }
        }
        return null;
    }
}
